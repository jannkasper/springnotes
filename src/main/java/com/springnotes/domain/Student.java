package com.springnotes.domain;

public class Student {
    private int no;
    private String name;

    // standard constructors, getters and setters

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
